class DivisibleByFive {

    int number;

    void check() {
        if (number % 5 == 0)
            System.out.println("Divisible by 5");
        else
            System.out.println("Not Divisible by 5");
    }

    public static void main(String[] args) {
        DivisibleByFive obj = new DivisibleByFive();
        obj.number = 25;
        obj.check();
    }
}